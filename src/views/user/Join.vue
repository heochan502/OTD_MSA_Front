const timerDisplay = computed(() => {
  const minutes = Math.floor(emailTimer.value / 60);
  const seconds = emailTimer.value % 60;
  return `${minutes}:${seconds.toString().padStart(2, '0')}`;
});

// 이메일 인증번호 전송
const sendVerificationEmail = async () => {
  if (!email.value) return;

  isLoading.value = true;
  try {
    const response = await fetch(
      `${basePath}/api/OTD/email/send-verification`,
      {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ email: email.value }),
      }
    );

    if (response.ok) {
      isEmailSent.value = true;
      emailTimer.value = 300;
      showAlert('전송 완료', '인증코드가 발송되었습니다.', 'success');
    } else {
      const error = await response.json();
      showAlert(
        '전송 실패',
        error.message || '이메일 발송에 실패했습니다.',
        'error'
      );
    }
  } catch (error) {
    showAlert('네트워크 오류', '네트워크 오류가 발생했습니다.', 'error');
  }
  isLoading.value = false;
};

// 이메일 인증번호 확인
const verifyEmailCode = async () => {
  if (verificationCode.value.length !== 6) return;

  isLoading.value = true;
  try {
    const response = await fetch(`${basePath}/api/OTD/email/verify-code`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        email: email.value,
        code: verificationCode.value,
      }),
    });

    const result = await response.json();

    if (response.ok && result.result?.verified === true) {
      isEmailVerified.value = true;
      showAlert('인증 완료', '이메일 인증이 완료되었습니다.', 'success');
    } else {
      showAlert(
        '인증 실패',
        result.message || '인증코드가 일치하지 않습니다.',
        'error'
      );
    }
  } catch (error) {
    showAlert('네트워크 오류', '네트워크 오류가 발생했습니다.', 'error');
  }
  isLoading.value = false;
